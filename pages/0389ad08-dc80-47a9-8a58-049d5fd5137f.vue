 может получить свое продолжение в виде подострого воспалительного процесса на более глубоком уровне.</p>
 <p style="text-align: justify;">Следовательно, с самого детства все острые воспалительные состояния
 следует лечить очень осмотрительно и тщательно, и не заставлять иммунную систему отступать. Также 
 автор в данной статье выдвигает в качестве предположения идею о том, что все хронические
 дегенеративные процессы носят подострый воспалительный характер, и что "воспаление" как таковое
 является основным общим параметром для всех заболеваний.</p>
 <p style="text-align: left;"><b>Основные положения</b></p>
 <p style="text-align: justify;">На протяжении жизни человека с момента рождения и до самой смерти здоровье 
 индивидуума подвергается череде болезней, острых и хронических – континууму. В том случае, когда
 острые заболевания не лечатся должным образом и иммунная система пациента ослаблена, то здоровье индивида
 находится в постоянной опасности.</p>
 <p style="text-align: justify;">Острые детские заболевания (не эпидемические, которые в первую очередь
 проявляются в системах внешнего реагирования организма - в респираторной, пищеварительной и
 эпидермальной - кожной) необходимо лечить осторожно и не подавлять избытком лекарств. В противном случае,
 острые заболевания продолжатся в измененной форме, в виде подострого воспалительного процесса, запуская
 экспрессию генетических предрасположенностей организма, вследствие чего манифестируют хронические
 дегенеративные заболевания. Все хронические состояния также носят воспалительный характер, и это
 "воспаление" является отличительным признаком всех заболеваний.</p>
 <p style="text-align: justify;">Если на организм в состоянии жара (высокой температуры) неоднократно оказывается
 агрессивная нагрузка сильными химическими препаратами или их избытком, то позже и со временем, ослабленная
 иммунная система может быть подорвана настолько, что уже не сможет больше реагировать сильным жаром, даже
 при воздействии опасных микроорганизмов. Одним из лучших примеров этого является синдром хронической
