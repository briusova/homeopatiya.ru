 вирусной инфекции может развиться хроническое состояние, которое иногда характеризуется изнуряющей
 усталостью, сопровождаемой мышечной слабостью, небольшим повышением температуры, болезненностью
 лимфатических узлов, головной болью и депрессией. Другим примером является острый вирусный гепатит, который
 может развиться в дисфункцию печени и в итоге привести к циррозу печени, а также острая ревматическая
 атака, заканчивающаяся хронической болезнью сердца. Известно также, что у пожилых людей снижена способность
 реагирования высокой температурой на воздействие возбудителя инфекции. Во всех таких случаях, когда высокая
 температура как реакция на вирусную инфекцию резко подавляется соответствующими средствами, и общий уровень
 здоровья серьезно ухудшается. При этом провоцируется появление нового хронического дегенеративного
 заболевания, к которому организм генетически предрасположен.</p>
 <p style="text-align: justify;">Возникают вопросы: "Какова связь между острыми воспалительными процессами и
 хроническими заболеваниями, для которых характерны обострения?" и "Могут ли они образовывать одну линию
 основного нарушения?".</p>
 <p style="text-align: justify;">В острых состояниях, после агрессивного вмешательства сильных химических
 веществ, общее состояние здоровья организма будет понижаться, организм «сдает свои позиции» защиты на
 периферическом уровне, и отступая , пытается продолжить защиту на более глубоком. Если эта
 новая линия защиты опять атакуется, то защита организма спустится на еще более глубокий уровень. Понижение
 уровня защиты будет следовать иерархическому плану, который, вероятно, является архетипом для всех людей —
 периферические инфекции идут вглубь, на более значимый уровень.</p>
 <p style="text-align: justify;">Следовательно, мы можем предположить, что иммунная система, главной задачей
 которой является поддержание жизни любой ценой, структурирована так, чтобы откликаться и реагировать разными
 способами на разных уровнях. У иммунной системы есть несколько уровней защиты. Первая линия защиты 
 - это способность повышать высокую температуру на заболевание. Если это уже невозможно из-за
 иммуннодефицита, тогда второй линией защиты будет подострый воспалительный процесс, который носит более
 размытый характер, процесс затрагивает большое количество систем организма и поэтому сильнее подрывает
 здоровье, вовлекая все более жизненно важные органы или системы.</p>
 <p style="text-align: left;"><b>Бесконечная сложность человеческого существа</b></p>
 <p style="text-align: justify;">Если мы рассматриваем человеческое существо как целое, с разумом, чувствами,
 языком, познавательными и созидательными возможностями, то оказывается, что <b>никакой другой организм на
 этой
